function Personagem(nome, altura, peso, nascimento, genero, corCabelo, corPele, corOlhos, homeworld, films, starships, vehicles) {
    this.nome = nome;
    this.altura = altura;
    this.peso = peso;
    this.nascimento = nascimento;
    this.genero = genero;
    this.corCabelo = corCabelo;
    this.corPele = corPele;
    this.corOlhos = corOlhos;
    this.homeworld = homeworld;
    this.films = films;
    this.starships = starships;
    this.vehicles = vehicles;
}